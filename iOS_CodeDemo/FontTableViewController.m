#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return self.dataSource.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    static NSString *res = @"res";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:res];
    
    if (!cell) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleValue1 reuseIdentifier:res];
        cell.selectionStyle = UITableViewCellSelectionStyleNone;
    }
    
    NSString *fontName = self.dataSource[indexPath.row];
    cell.textLabel.text = fontName;
    cell.textLabel.font = [UIFont fontWithName:fontName size:14];
    cell.detailTextLabel.text = fontName;
    cell.detailTextLabel.font = [UIFont systemFontOfSize:12];
    
    return cell;
}


